package com.codingtest;

import java.util.*;


/**
 * =============================================================================
 * 1. 문제 (기능개발)
 * =============================================================================
 * 프로그래머스 팀에서는 기능 개선 작업을 수행 중입니다. 각 기능은 진도가 100%일 때 서비스에 반영할 수 있습니다.
 *
 * 각 기능의 개발속도는 모두 다르기 때문에 뒤에 있는 기능이 앞에 있는 기능보다 먼저 개발될 수 있고,
 * 이때 뒤에 있는 기능은 앞에 있는 기능이 배포될 때 함께 배포됩니다.
 *
 * 먼저 배포되어야 하는 순서대로 작업의 진도가 적힌 정수 배열 progresses와
 * 각 작업의 개발 속도가 적힌 정수 배열 speeds가 주어질 때
 * 각 배포마다 몇 개의 기능이 배포되는지를 return 하도록 solution 함수를 완성하세요.
 *
 * [제한사항]
 * - 작업의 개수(progresses, speeds 배열의 길이)는 100개 이하입니다.
 * - 작업 진도는 100 미만의 자연수입니다.
 * - 작업 속도는 100 이하의 자연수입니다.
 * - 배포는 하루에 한 번만 할 수 있으며, 하루의 끝에 이루어진다고 가정합니다.
 *
 * =============================================================================
 * 2. 예시 입·출력
 * =============================================================================
 * | progresses              | speeds             | return  |
 * |--------------------------|--------------------|---------|
 * | [93, 30, 55]             | [1, 30, 5]         | [2, 1]  |
 * | [95, 90, 99, 99, 80, 99]| [1, 1, 1, 1, 1, 1] | [1, 3, 2] |
 *
 * 입출력 예 #1: 7일째 2개, 9일째 1개 배포 → [2, 1]
 * 입출력 예 #2: 5일째 1개, 10일째 3개, 20일째 2개 배포 → [1, 3, 2]
 *
 * =============================================================================
 * 3. 방향을 어떻게 잡아야 할지 고민
 * =============================================================================
 * - 배포 순서는 정해져 있음 → 앞 기능이 배포될 때까지 뒤 기능은 기다려야 함.
 * - 각 기능이 "몇 일 뒤에 완료되는지"를 먼저 구함: ceil((100 - progress) / speed).
 * - 순서대로 보면서, "오늘 배포일"을 정했을 때 그날 또는 그보다 먼저 완료된 연속된 기능들을 한 번에 배포.
 * - 즉, 남은 일수 배열을 순회하며 "현재 배포일 이하인 것"을 묶어서 카운트하고, 다음 미완료부터 다시 배포일을 잡음.
 *
 * =============================================================================
 * 4. 설명 (왜 이렇게 코드를 짰는지)
 * =============================================================================
 * - 각 작업의 남은 일수: days[i] = (100 - progresses[i] + speeds[i] - 1) / speeds[i] (올림)
 * - 인덱스 i부터: releaseDay = days[i], 그날 배포할 개수 = days[i] 이하인 연속된 원소 개수. 카운트만큼 result에 추가 후 인덱스 이동.
 * - List를 int[]로 변환해 반환. 시간 O(n), 공간 O(n).
 */
public class queue_level1_01 {

    public int[] solution(int[] progresses, int[] speeds) {
        Queue<Integer> q = new LinkedList<>();
        List<Integer> answerList = new ArrayList<>();

        for (int i = 0; i < speeds.length; i++) {
            //끝나는 날짜 미리 계산 30 , 30일 때 
            double remain = (double)(100 - progresses[i]) / speeds[i]; // 2.333
            int date = (int) Math.ceil(remain);  // 3

            if (!q.isEmpty() && q.peek() < date) {
                answerList.add(q.size());
                q.clear();
            }

            q.offer(date);
        }
        // 마지막에 남은 큐 적재
        answerList.add(q.size());

        int[] answer = new int[answerList.size()];

        for (int i = 0; i < answer.length; i++) {
            answer[i] = answerList.get(i);
        }

        return answer;
    }

    public int[] solution2(int[] progresses, int[] speeds) {
        int[] dayOfend = new int[100];
        int day = -1;
        for(int i=0; i<progresses.length; i++) {
            while(progresses[i] + (day*speeds[i]) < 100) {
                day++;
            }
            dayOfend[day]++;
        }
        return Arrays.stream(dayOfend).filter(i -> i!=0).toArray();
    }
}
