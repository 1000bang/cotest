package com.codingtest;

import java.util.Arrays;

/**
 * =============================================================================
 * 1. 문제 (K번째수)
 * =============================================================================
 * 배열 array의 i번째 숫자부터 j번째 숫자까지 자르고 정렬했을 때, k번째에 있는 수를 구하려 합니다.
 *
 * 예: array = [1, 5, 2, 6, 3, 7, 4], i = 2, j = 5, k = 3
 *     → 2번째~5번째 자르면 [5, 2, 6, 3], 정렬하면 [2, 3, 5, 6], 3번째 수 = 5
 *
 * 배열 array와 [i, j, k]를 원소로 가진 2차원 배열 commands가 주어질 때,
 * commands의 모든 원소에 대해 위 연산을 적용한 결과를 배열에 담아 return 하세요.
 *
 * [제한사항]
 * - array의 길이: 1 이상 100 이하
 * - array의 각 원소: 1 이상 100 이하
 * - commands의 길이: 1 이상 50 이하
 * - commands의 각 원소는 길이 3 (i, j, k). 문제에서 i, j, k는 1번째부터 시작하는 번호.
 *
 * =============================================================================
 * 2. 예시 입·출력
 * =============================================================================
 * | array                  | commands                      | return      |
 * |------------------------|--------------------------------|-------------|
 * | [1, 5, 2, 6, 3, 7, 4] | [[2, 5, 3], [4, 4, 1], [1, 7, 3]] | [5, 6, 3] |
 *
 * [2,5,3]: 2~5번째 [5,2,6,3] → 정렬 [2,3,5,6] → 3번째 = 5
 * [4,4,1]: 4~4번째 [6] → 1번째 = 6
 * [1,7,3]: 1~7번째 전체 정렬 [1,2,3,4,5,6,7] → 3번째 = 3
 *
 * =============================================================================
 * 3. 방향을 어떻게 잡아야 할지 고민
 * =============================================================================
 * - i, j, k는 "번째"이므로 1부터 시작. 배열 인덱스로는 i-1 ~ j-1 구간, k번째 = 인덱스 k-1.
 * - 각 command마다 array의 (i-1)~(j-1) 구간을 복사 → 정렬 → (k-1)번 인덱스 값을 결과에 추가.
 *
 * =============================================================================
 * 4. 설명 (왜 이렇게 코드를 짰는지)
 * =============================================================================
 * - commands를 순회하며, Arrays.copyOfRange(array, i-1, j)로 구간 복사 (j는 미포함이므로 j번째 포함하려면 end=j).
 * - 문제: "2번째부터 5번째까지" = 인덱스 1,2,3,4 → copyOfRange(array, 1, 5) (5는 미포함이므로 1~4 인덱스).
 * - 복사한 배열 정렬 후 [k-1] 값을 answer에 넣음.
 */
public class sort_level1_01 {

    public int[] solution(int[] array, int[][] commands) {
        int[] answer = new int[commands.length];

        for (int i = 0; i < commands.length; i++) {
            int start = commands[i][0];
            int end = commands[i][1];
            int k = commands[i][2];

            int[] slice = Arrays.copyOfRange(array, start - 1, end);
            Arrays.sort(slice);
            answer[i] = slice[k - 1];
        }

        return answer;
    }
}

