package com.codingtest;

import java.util.HashMap;

/**
 * =============================================================================
 * 1. 문제 (의상)
 * =============================================================================
 * 코니는 매일 다른 옷을 조합하여 입는 것을 좋아합니다.
 *
 * 예를 들어 코니가 가진 옷이 아래와 같고, 오늘 코니가 동그란 안경, 긴 코트, 파란색 티셔츠를 입었다면
 * 다음날은 청바지를 추가로 입거나 동그란 안경 대신 검정 선글라스를 착용하거나 해야 합니다.
 *
 *   종류   | 이름
 *   얼굴   | 동그란 안경, 검정 선글라스
 *   상의   | 파란색 티셔츠
 *   하의   | 청바지
 *   겉옷   | 긴 코트
 *
 * 코니는 각 종류별로 최대 1가지 의상만 착용할 수 있습니다.
 * (예: 동그란 안경과 검정 선글라스를 동시에 착용할 수는 없습니다.)
 *
 * 착용한 의상의 일부가 겹치더라도, 다른 의상이 겹치지 않거나, 혹은 의상을 추가로 더 착용한 경우에는
 * 서로 다른 방법으로 옷을 착용한 것으로 계산합니다.
 * 코니는 하루에 최소 한 개의 의상은 입습니다.
 *
 * 코니가 가진 의상들이 담긴 2차원 배열 clothes가 주어질 때
 * 서로 다른 옷의 조합의 수를 return 하도록 solution 함수를 작성해주세요.
 *
 * [제한사항]
 * - clothes의 각 행은 [의상의 이름, 의상의 종류]로 이루어져 있습니다.
 * - 코니가 가진 의상의 수는 1개 이상 30개 이하입니다.
 * - 같은 이름을 가진 의상은 존재하지 않습니다.
 * - clothes의 모든 원소는 문자열로 이루어져 있습니다.
 * - 모든 문자열의 길이는 1 이상 20 이하인 자연수이고, 알파벳 소문자 또는 '_' 로만 이루어져 있습니다.
 *
 * =============================================================================
 * 2. 예시 입·출력
 * =============================================================================
 * | clothes                                                                 | return |
 * |-------------------------------------------------------------------------|--------|
 * | [["yellow_hat", "headgear"], ["blue_sunglasses", "eyewear"], ["green_turban", "headgear"]] | 5 |
 * | [["crow_mask", "face"], ["blue_sunglasses", "face"], ["smoky_makeup", "face"]]             | 3 |
 *
 * =============================================================================
 * 3. 방향을 어떻게 잡아야 할지 고민
 * =============================================================================
 * 1) 종류별 개수: HashMap<종류, count> 에 넣는 건 맞음. (이미 하신 것)
 * 2) 각 종류에서 할 수 있는 선택:
 *    - "이 종류에서 하나 고르기" (count가지) + "이 종류는 안 입기" (1가지) = (count + 1)가지
 * 3) 모든 종류의 선택지를 곱하면 → 그게 "조합의 총 개수"인데,
 *    이때 "모든 종류를 다 안 입는 경우"도 포함되어 있음.
 * 4) 조건: 최소 1개는 입어야 하므로 → (곱한 값) - 1 이 정답.
 *
 * 예) headgear 2개, eyewear 1개
 *     → (2+1) * (1+1) = 6, 여기서 "아무것도 안 입는 1가지" 빼면 5.
 *
 * =============================================================================
 * 4. 설명 (왜 이렇게 코드를 짰는지)
 * =============================================================================
 * - HashMap<종류, count>로 clothes를 한 번 돌며 종류별 개수를 셉니다. (cloth[1]이 종류)
 * - answer = 1에서 시작해, map의 각 count에 대해 answer *= (count + 1)을 합니다.
 *   (각 종류에서 "하나 고르기 또는 안 입기" = count+1 가지이므로, 모두 곱하면 전체 조합 수)
 * - 그 값에는 "아무것도 안 입는 경우" 1가지가 포함되어 있으므로, return answer - 1 합니다.
 * - 시간 O(clothes 길이), 공간 O(종류 수).
 */
public class hash_level2_02 {

    public int solution(String[][] clothes) {
        HashMap<String, Integer> map = new HashMap<>();
        for (String[] cloth : clothes) {
            map.put(cloth[1], map.getOrDefault(cloth[1], 0) + 1);
        }
        int answer = 1;
        for (int count : map.values()) {
            answer *= (count + 1);
        }
        return answer - 1;
    }
}
