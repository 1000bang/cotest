package com.codingtest;

import java.util.Arrays;
import java.util.HashSet;
import java.util.Set;
import java.util.stream.Collectors;

/**
 * =============================================================================
 * 1. 문제 (폰켓몬)
 * =============================================================================
 * 당신은 폰켓몬을 잡기 위한 오랜 여행 끝에, 홍 박사님의 연구실에 도착했습니다.
 * 홍 박사님은 당신에게 자신의 연구실에 있는 총 N 마리의 폰켓몬 중에서 N/2마리를 가져가도 좋다고 했습니다.
 *
 * 홍 박사님 연구실의 폰켓몬은 종류에 따라 번호를 붙여 구분합니다. 따라서 같은 종류의 폰켓몬은 같은 번호를 가지고 있습니다.
 * 예를 들어 연구실에 총 4마리의 폰켓몬이 있고, 각 폰켓몬의 종류 번호가 [3번, 1번, 2번, 3번]이라면
 * 이는 3번 폰켓몬 두 마리, 1번 폰켓몬 한 마리, 2번 폰켓몬 한 마리가 있음을 나타냅니다.
 *
 * 당신은 최대한 다양한 종류의 폰켓몬을 가지길 원하기 때문에, 최대한 많은 종류의 폰켓몬을 포함해서 N/2마리를 선택하려 합니다.
 * N마리 폰켓몬의 종류 번호가 담긴 배열 nums가 매개변수로 주어질 때, N/2마리의 폰켓몬을 선택하는 방법 중,
 * 가장 많은 종류의 폰켓몬을 선택하는 방법을 찾아, 그때의 폰켓몬 종류 번호의 개수를 return 하도록 solution 함수를 완성해주세요.
 *
 * [제한사항]
 * - nums는 폰켓몬의 종류 번호가 담긴 1차원 배열입니다.
 * - nums의 길이(N)는 1 이상 10,000 이하의 자연수이며, 항상 짝수로 주어집니다.
 * - 폰켓몬의 종류 번호는 1 이상 200,000 이하의 자연수로 나타냅니다.
 * - 가장 많은 종류의 폰켓몬을 선택하는 방법이 여러 가지인 경우에도, 선택할 수 있는 폰켓몬 종류 개수의 최댓값 하나만 return 하면 됩니다.
 *
 * =============================================================================
 * 2. 예시 입·출력
 * =============================================================================
 * | nums              | result |
 * |-------------------|--------|
 * | [3, 1, 2, 3]      | 2      |
 * | [3, 3, 3, 2, 2, 4]| 3      |
 * | [3, 3, 3, 2, 2, 2]| 2      |
 *
 * =============================================================================
 * 3. 방향을 어떻게 잡아야 할지 고민
 * =============================================================================
 * - N/2마리만 가져갈 수 있으므로, 아무리 종류가 많아도 "가져가는 수"는 최대 N/2개.
 * - 실제로 있는 "서로 다른 종류의 수"를 세면, 그게 최대 종류 수의 상한이 됨.
 * - 따라서 정답 = min(서로 다른 종류 수, N/2).
 * - "서로 다른 종류"만 세면 되므로 Set(또는 Map의 키)을 쓰면 됨. HashSet으로 중복 제거 후 크기와 N/2 중 작은 값 반환.
 *
 * =============================================================================
 * 4. 설명 (왜 이렇게 코드를 짰는지)
 * =============================================================================
 * - HashSet에 nums의 원소를 넣으면 자동으로 종류 번호만 남고, size()가 "서로 다른 종류의 개수"가 됩니다.
 * - 가져갈 수 있는 마리 수는 N/2이므로, 최대 종류 수는 "종류 개수"와 "N/2" 중 더 작은 값입니다.
 * - Math.min(종류 개수, N/2)를 return 하면 됩니다.
 * - 한 번 순회로 Set을 채우므로 시간 O(N), Set 크기만큼 공간 O(종류 수)로 해결할 수 있습니다.
 */
public class hash_level1_02 {

    public int solution(int[] nums) {
        //Set을 사용하여 중복을 제거하고 총 종류의 수를 구함
        Set<Integer> kinds = new HashSet<>();
        for (int num : nums) {
            kinds.add(num);
        }
        //가져갈 수 있는 마리 수는 N/2
        int canTake = nums.length / 2;
        
        // 가져갈 수 있는 포켄못이랑 총 종류의 수 중 작은 값을 반환
        return Math.min(kinds.size(), canTake);
    }

    //스트림을 사용하여 중복을 제거하고 총 종류의 수를 구함
    public int solution2(int[] nums) {
        return Arrays.stream(nums)
                .boxed()
                .collect(Collectors.collectingAndThen(Collectors.toSet(),
                        phonekemons -> Integer.min(phonekemons.size(), nums.length / 2)));
    }
}
